#[cfg(test)]
use super::*;

#[test]
fn test_task1() {
    let s = concat!(
        "2413432311323\n",
        "3215453535623\n",
        "3255245654254\n",
        "3446585845452\n",
        "4546657867536\n",
        "1438598798454\n",
        "4457876987766\n",
        "3637877979653\n",
        "4654967986887\n",
        "4564679986453\n",
        "1224686865563\n",
        "2546548887735\n",
        "4322674655533\n",
    );
    let field = parse_field(s).unwrap();
    assert_eq!(task1(&field), 102);
}

